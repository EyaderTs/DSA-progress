class Solution(object):
    def maxProfit(self, prices):

        l,r = 0,1
        maxProfit = 0

        while r != len(prices):
            if prices[r] > prices[l]:
                profit = prices[r] - prices[l]
                maxProfit = max(profit,maxProfit)
            else:
                l=r
            r+=1
        return maxProfit
